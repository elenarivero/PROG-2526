package pruebas;

public class Pruebas {

	public static void main(String[] args) {
		int dia = 3;
		
		if(dia == 1) {
			System.out.println("Lunes");
		} 
		else if (dia == 2) {
			System.out.println("Martes");
		} else if (dia == 3) {
			System.out.println("Miércoles");
		} else if (dia == 4) {
			System.out.println("Jueves");
		} else {
			System.out.println("Valor no válido");
		}
	}

}
